package game.entities;

/**
 * Basic coordinates for {@code Charunka}.
 * @author lucasscottik.
 */
public class Coordinates {
	private int x;
	private int y;

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
}
